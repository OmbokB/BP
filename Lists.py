#lists, we use square brackets
fruits = ["Pineapple", "Orange", "Mango", "Orange"]
print(fruits[3])
#altering an item
fruits[0] = "Passion"
print(fruits)
#adding an item to a list
fruits.append("Banana")
print(fruits)
#inserting an item
fruits.insert(0, "Guava")
print(fruits)
#removing an item
fruits.remove("Orange")
print(fruits)
